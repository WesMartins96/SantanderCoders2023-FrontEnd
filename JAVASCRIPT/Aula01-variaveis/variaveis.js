// VARIÁVEL

// Uma variável em JavaScript pode ser comparada a uma caixa onde você pode guardar diferentes tipos de informações. Essas informações podem ser números, texto, datas ou qualquer outro tipo de dado que você queira armazenar.

// Você pode pensar em uma variável como um nome que você dá para essa caixa, para que você possa se referir a ela posteriormente. Por exemplo, você pode criar uma variável chamada "idade" e atribuir a ela o valor 26. Então, sempre que você usar o nome "idade" em seu código JavaScript, o programa saberá que você está se referindo ao valor 26.

// As variáveis também podem ser alteradas ao longo do tempo. Você pode atualizar o valor de uma variável atribuindo um novo valor a ela. Por exemplo, você pode alterar o valor da variável "idade" para 30 mais tarde no seu código.

// As variáveis são úteis porque permitem que você armazene e manipule dados de forma flexível em seu programa JavaScript. Elas são fundamentais para a programação, pois permitem que você armazene informações temporariamente durante a execução do seu código.


let idade = 26;
console.log(idade);

// Tipos de variáveis
let numero = 30;        // Number
idade = 27;             // Number
let altura = 1.68;      // Number
let nome = 'Wesley';    // String (Texto)
let isAnimal = false;   // Boolean (True ou False)

console.log(numero);
console.log(idade);
console.log(altura);
console.log(nome);
console.log(isAnimal);

// Usamos const para valores constante(que não é alterada)
const meuNome = 'Gael';
console.log(meuNome);

// var não é mais utilizado
var naoUsarVar = true;
console.log(naoUsarVar);
