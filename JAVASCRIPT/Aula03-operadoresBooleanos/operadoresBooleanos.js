// Operadores Booleanos


// Igualdade           == ou ===
// Desigualdade        !== ou !===
// maior que           >
// maior ou igual      >=
// menor que           <
// menor ou Igual      <=

const idade = 27;

console.log(idade > 18); //true
console.log(idade === "27"); //false
console.log(idade == '27'); //true



